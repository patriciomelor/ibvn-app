-- 1. ALTAS Y ALTERACIONES DE TABLAS EXISTENTES
-- Agregar columna mentor_id a la tabla de perfiles (FK circular a profiles)
ALTER TABLE public.profiles 
ADD COLUMN IF NOT EXISTS mentor_id UUID REFERENCES public.profiles(id) ON DELETE SET NULL;

-- 2. CREACIÓN DE NUEVAS TABLAS MINISTERIALES

-- Tabla de Misioneros Apoyados
CREATE TABLE IF NOT EXISTS public.missions_misioneros (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre TEXT NOT NULL,
    pais_servicio TEXT NOT NULL,
    historia TEXT NOT NULL,
    pedido_oracion TEXT NOT NULL,
    foto_url TEXT,
    contacto_email TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Actualizaciones y Cartas de Misioneros
CREATE TABLE IF NOT EXISTS public.missions_updates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    misionero_id BIGINT REFERENCES public.missions_misioneros(id) ON DELETE CASCADE NOT NULL,
    titulo TEXT NOT NULL,
    contenido TEXT NOT NULL,
    fecha DATE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Testimonios Misioneros (Motivar)
CREATE TABLE IF NOT EXISTS public.missions_testimonios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titulo TEXT NOT NULL,
    autor TEXT NOT NULL,
    contenido TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Viajes Misioneros Programados (Movilizar)
CREATE TABLE IF NOT EXISTS public.missions_viajes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    destino TEXT NOT NULL,
    fecha_inicio DATE NOT NULL,
    fecha_fin DATE NOT NULL,
    descripcion TEXT NOT NULL,
    requisitos TEXT NOT NULL,
    lider_id UUID REFERENCES public.profiles(id) ON DELETE SET NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Cursos de Preparación Misionera (Entrenar)
CREATE TABLE IF NOT EXISTS public.missions_cursos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titulo TEXT NOT NULL,
    descripcion TEXT NOT NULL,
    url_material TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Compromisos de Oración por Pueblos No Alcanzados
CREATE TABLE IF NOT EXISTS public.missions_oracion_registro (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
    pueblo_id BIGINT REFERENCES public.missions_pueblos(id) ON DELETE CASCADE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    UNIQUE(user_id, pueblo_id)
);

