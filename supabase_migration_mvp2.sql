ALTER TABLE public.missions_cursos ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.missions_oracion_registro ENABLE ROW LEVEL SECURITY;

-- =========================================================================
-- 4. POLÍTICAS DE ACCESO (RLS POLICIES)
-- =========================================================================

-- --- MISIONEROS ---
CREATE POLICY "Permitir ver misioneros a usuarios autenticados" ON public.missions_misioneros
    FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "Permitir administrar misioneros a pastores" ON public.missions_misioneros
    FOR ALL USING (public.is_pastor_admin());

-- --- ACTUALIZACIONES ---
CREATE POLICY "Permitir ver actualizaciones a usuarios autenticados" ON public.missions_updates
    FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "Permitir administrar actualizaciones a pastores y líderes" ON public.missions_updates
    FOR ALL USING (public.is_lider());

-- --- TESTIMONIOS ---
CREATE POLICY "Permitir ver testimonios a usuarios autenticados" ON public.missions_testimonios
    FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "Permitir administrar testimonios a pastores y líderes" ON public.missions_testimonios
    FOR ALL USING (public.is_lider());

-- --- VIAJES MISIONEROS ---
CREATE POLICY "Permitir ver viajes a usuarios autenticados" ON public.missions_viajes
    FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "Permitir administrar viajes a pastores y líderes" ON public.missions_viajes
    FOR ALL USING (public.is_lider());

-- --- CURSOS ---
CREATE POLICY "Permitir ver cursos a usuarios autenticados" ON public.missions_cursos
    FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "Permitir administrar cursos a pastores y líderes" ON public.missions_cursos
    FOR ALL USING (public.is_lider());

-- --- COMPROMISOS DE ORACIÓN ---
CREATE POLICY "Permitir ver compromisos de oración propios" ON public.missions_oracion_registro
    FOR SELECT USING (auth.uid() = user_id OR public.is_pastor_admin());

CREATE POLICY "Permitir registrar compromiso propio" ON public.missions_oracion_registro
    FOR INSERT WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Permitir borrar compromiso propio" ON public.missions_oracion_registro
    FOR DELETE USING (auth.uid() = user_id);

