-- 1. CREACIÓN DE TABLAS BASE
-- =========================================================================

-- Tabla de Células (grupos pequeños)
CREATE TABLE IF NOT EXISTS public.celulas (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre TEXT NOT NULL,
    lider_id UUID, -- Se vincula a profiles más adelante por FK circular
    dia TEXT,
    hora TEXT,
    lugar TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Ministerios
CREATE TABLE IF NOT EXISTS public.ministerios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre TEXT NOT NULL,
    descripcion TEXT,
    coordinador_id UUID, -- Se vincula a profiles más adelante por FK circular
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Perfiles (Extiende a auth.users de Supabase)
CREATE TABLE IF NOT EXISTS public.profiles (
    id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
    email TEXT UNIQUE NOT NULL,
    nombre TEXT NOT NULL,
    rol TEXT DEFAULT 'miembro'::text NOT NULL CHECK (rol IN ('miembro', 'lider', 'pastor_admin')),
    celula_id BIGINT REFERENCES public.celulas ON DELETE SET NULL,
    ministerio_id BIGINT REFERENCES public.ministerios ON DELETE SET NULL,
    tel TEXT,
    direccion TEXT,
    fecha_nacimiento DATE,
    fecha_ingreso DATE,
    como_llego TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Agregar Foreign Keys circulares para los líderes/coordinadores ahora que la tabla de perfiles existe
ALTER TABLE public.celulas ADD CONSTRAINT fk_celulas_lider FOREIGN KEY (lider_id) REFERENCES public.profiles(id) ON DELETE SET NULL;
ALTER TABLE public.ministerios ADD CONSTRAINT fk_ministerios_coordinador FOREIGN KEY (coordinador_id) REFERENCES public.profiles(id) ON DELETE SET NULL;

-- Tabla de Historial Espiritual (ciclo de clases)
CREATE TABLE IF NOT EXISTS public.spiritual_records (
    user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE PRIMARY KEY,
    bautizado BOOLEAN DEFAULT false NOT NULL,
    fecha_bautismo DATE,
    conectar BOOLEAN DEFAULT false NOT NULL, -- Clase Conectar
    crecer BOOLEAN DEFAULT false NOT NULL,   -- Clase Crecer
    intro_lid BOOLEAN DEFAULT false NOT NULL, -- Clase Intro Liderazgo
    dones BOOLEAN DEFAULT false NOT NULL,     -- Clase Descubre tus Dones
    aplicadas BOOLEAN DEFAULT false NOT NULL, -- Clase Herramientas Aplicadas
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Devocionales Publicados
CREATE TABLE IF NOT EXISTS public.devocionales (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    semana INTEGER NOT NULL,
    titulo TEXT NOT NULL,
    texto_biblico TEXT NOT NULL, -- Ejemplo: "Filipenses 4:6-7"
    reflexion TEXT NOT NULL,
    oracion TEXT NOT NULL,
    pdf_url TEXT,
    published_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla del Diario Devocional Personal (Journaling)
CREATE TABLE IF NOT EXISTS public.devotional_journal (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
    devocional_id BIGINT REFERENCES public.devocionales(id) ON DELETE CASCADE NOT NULL,
    apreciacion TEXT NOT NULL,       -- Bloque 1: Lo que Dios me dijo
    cambios TEXT NOT NULL,            -- Bloque 2: Cosas a cambiar
    oracion_personal TEXT NOT NULL,   -- Bloque 3: Motivos de oración
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    UNIQUE(user_id, devocional_id)
);

-- Tabla de Notas Pastorales Privadas (Acceso exclusivo a pastores)
CREATE TABLE IF NOT EXISTS public.pastoral_notes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
    author_id UUID REFERENCES public.profiles(id) ON DELETE SET NULL NOT NULL,
    content TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Alertas de Seguimiento Pastoral
CREATE TABLE IF NOT EXISTS public.people_alerts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE NOT NULL,
    tipo TEXT NOT NULL, -- Ejemplo: "Inactividad", "Apoyo Espiritual", "Salud"
    descripcion TEXT NOT NULL,
    resuelta BOOLEAN DEFAULT false NOT NULL,
    created_by UUID REFERENCES public.profiles(id) ON DELETE SET NULL NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Tabla de Pueblos No Alcanzados (Misiones)
CREATE TABLE IF NOT EXISTS public.missions_pueblos (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre TEXT NOT NULL,
    region TEXT NOT NULL,
    poblacion INTEGER,
    religion TEXT,
    porcentaje_cristiano NUMERIC(5,2),
    activo BOOLEAN DEFAULT true NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

