-- 5. TRIGGER DE CREACIÓN AUTOMÁTICA DE PERFIL AL REGISTRARSE
-- =========================================================================

-- Función del disparador
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  -- Insertar perfil de usuario
  INSERT INTO public.profiles (id, email, nombre, rol)
  VALUES (
    new.id,
    new.email,
    COALESCE(new.raw_user_meta_data->>'nombre', new.raw_user_meta_data->>'name', 'Miembro Nuevo'),
    'miembro'
  );

  -- Insertar registro espiritual vacío por defecto
  INSERT INTO public.spiritual_records (user_id)
  VALUES (new.id);

  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Trigger asociado a auth.users
CREATE OR REPLACE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

