-- 3. FUNCIONES HELPER DE ROLES (SECURITY DEFINER para evitar recursión RLS)
-- =========================================================================

-- Verifica si el usuario actual es 'pastor_admin'
CREATE OR REPLACE FUNCTION public.is_pastor_admin()
RETURNS BOOLEAN SECURITY DEFINER AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.profiles
    WHERE id = auth.uid() AND rol = 'pastor_admin'
  );
END;
$$ LANGUAGE plpgsql;

-- Verifica si el usuario actual es 'lider' o superior
CREATE OR REPLACE FUNCTION public.is_lider()
RETURNS BOOLEAN SECURITY DEFINER AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.profiles
    WHERE id = auth.uid() AND rol IN ('lider', 'pastor_admin')
  );
END;
$$ LANGUAGE plpgsql;

