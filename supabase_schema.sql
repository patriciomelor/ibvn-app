-- 4. POLÍTICAS DE ACCESO (RLS POLICIES)
-- =========================================================================

-- --- PROFILES ---
CREATE POLICY "Permitir lectura de perfil propio" ON public.profiles
    FOR SELECT USING (auth.uid() = id);

CREATE POLICY "Permitir lectura de perfiles a líderes y pastores" ON public.profiles
    FOR SELECT USING (public.is_lider());

CREATE POLICY "Permitir actualización de perfil propio" ON public.profiles
    FOR UPDATE USING (auth.uid() = id) WITH CHECK (auth.uid() = id);

CREATE POLICY "Permitir a pastores administrar perfiles y roles" ON public.profiles
    FOR ALL USING (public.is_pastor_admin());

-- --- CÉLULAS ---
CREATE POLICY "Permitir ver células a usuarios autenticados" ON public.celulas
